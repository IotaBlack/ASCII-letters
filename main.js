input.addEventListener('input', e => {
    output.innerHTML = toASCIILetters(input.value).replace(/\ /g, "&nbsp;").replace(/\n/g, '<br>')
})

copyBtn.addEventListener('click', e => {
    copyfield.innerText = (discord.checked)?"```"+toASCIILetters(input.value)+"```":toASCIILetters(input.value)
    copyfield.select()
    document.execCommand('copy')
})

/** @param {string} src */
function toASCIILetters(src) {
    src = src.toUpperCase().split("\n")
    var str = ""

    for (let k = 0; k < src.length; k++) {
        for (let i = 0; i < 6; i++) {
            for (let j = 0; j < src[k].length; j++) {
                str += (letters[src[k][j]] || ["", "", "", "", "", "", ])[i]
            }
            str += "\n"
        }
        //str += "\n"
    }

    return str
}